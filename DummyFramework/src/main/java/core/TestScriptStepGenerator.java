package core;

public class TestScriptStepGenerator {
	private String Reference;
	private String AutomationScriptName;
	private String StepKeyword;
	private String StepGroup;
	private String SkipStep;
	private String ReferenceStepKeywordSheetName;
	private String SerialNoStepKeyword;
	
	
	public String getSerialNoStepKeyword() {
		return SerialNoStepKeyword;
	}
	public void setSerialNoStepKeyword(String serialNoStepKeyword) {
		SerialNoStepKeyword = serialNoStepKeyword;
	}
	public String getReference() {
		return Reference;
	}
	public void setReference(String reference) {
		Reference = reference;
	}
	public String getAutomationScriptName() {
		return AutomationScriptName;
	}
	public void setAutomationScriptName(String automationScriptName) {
		AutomationScriptName = automationScriptName;
	}
	public String getStepKeyword() {
		return StepKeyword;
	}
	public void setStepKeyword(String stepKeyword) {
		StepKeyword = stepKeyword;
	}
	public String getStepGroup() {
		return StepGroup;
	}
	public void setStepGroup(String stepGroup) {
		StepGroup = stepGroup;
	}
	public String getSkipStep() {
		return SkipStep;
	}
	public void setSkipStep(String skipStep) {
		SkipStep = skipStep;
	}
	public String getReferenceStepKeywordSheetName() {
		return ReferenceStepKeywordSheetName;
	}
	public void setReferenceStepKeywordSheetName(String referenceStepKeywordSheetName) {
		ReferenceStepKeywordSheetName = referenceStepKeywordSheetName;
	}
}
